'use client'

import { useState, useEffect } from 'react'
import { createClient } from '@/lib/supabase/client'
import { motion, AnimatePresence } from 'framer-motion'
import { 
  Video, Plus, Clock, CheckCircle, ChevronLeft, User, Play,
  Sparkles, ExternalLink, X, Search, Heart, Users, Check,
  Copy, Mail, MessageSquare
} from 'lucide-react'
import Link from 'next/link'

interface Contact {
  id: string
  full_name: string
  phone: string
  email: string
}

interface Circle {
  id: string
  name: string
  color: string
  members: { contact_id: string }[]
}

// Note: Circles are for user collaboration, not contact grouping
// Contact groups feature could be added later for interview targeting

interface Question {
  id: string
  question_text: string
  category: string
  is_system: boolean
  is_favorite: boolean
}

interface Session {
  id: string
  title: string
  status: string
  access_token: string
  created_at: string
  interview_group_id?: string
  group_question?: string
  contact: {
    id: string
    full_name: string
  }
  session_questions: {
    id: string
    question_text: string
    status: string
  }[]
  video_responses: {
    id: string
    duration: number
    ai_summary: string
  }[]
}

interface GroupedSession {
  group_id: string
  question: string
  sessions: Session[]
  created_at: string
}

const CATEGORIES = [
  { id: 'childhood', label: 'Childhood', emoji: 'üëí' },
  { id: 'relationships', label: 'Relationships', emoji: '‚ù§Ô∏è' },
  { id: 'career', label: 'Career', emoji: 'üíº' },
  { id: 'wisdom', label: 'Wisdom', emoji: 'ü¶â' },
  { id: 'adversity', label: 'Challenges', emoji: 'üí™' },
  { id: 'fun', label: 'Fun', emoji: 'üéâ' },
  { id: 'history', label: 'History', emoji: 'üìú' },
  { id: 'spirituality', label: 'Faith', emoji: 'üôè' },
  { id: 'custom', label: 'My Questions', emoji: '‚ú®' },
]

type RecipientMode = 'single' | 'multiple' | 'circle'

export default function JournalistPage() {
  const [sessions, setSessions] = useState<Session[]>([])
  const [contacts, setContacts] = useState<Contact[]>([])
  const [circles, setCircles] = useState<Circle[]>([])
  const [questions, setQuestions] = useState<Question[]>([])
  const [loading, setLoading] = useState(true)
  
  // Modal state
  const [showNewSession, setShowNewSession] = useState(false)
  const [step, setStep] = useState<'recipients' | 'question'>('recipients')
  
  // Recipient selection
  const [recipientMode, setRecipientMode] = useState<RecipientMode>('single')
  const [selectedCircle, setSelectedCircle] = useState<Circle | null>(null)
  const [selectedContacts, setSelectedContacts] = useState<Contact[]>([])
  
  // Question selection
  const [selectedQuestion, setSelectedQuestion] = useState<string | null>(null)
  const [selectedCategory, setSelectedCategory] = useState<string | null>(null)
  const [customQuestion, setCustomQuestion] = useState('')
  
  const [creating, setCreating] = useState(false)
  const [copied, setCopied] = useState<string | null>(null)
  
  const supabase = createClient()

  useEffect(() => {
    loadData()
  }, [])

  const loadData = async () => {
    setLoading(true)
    const { data: { user } } = await supabase.auth.getUser()
    if (!user) return

    const [sessionsRes, contactsRes, questionsRes, circlesRes] = await Promise.all([
      supabase
        .from('interview_sessions')
        .select(`
          *, contact:contacts(id, full_name),
          session_questions(id, question_text, status),
          video_responses(id, duration, ai_summary)
        `)
        .eq('user_id', user.id)
        .order('created_at', { ascending: false }),
      supabase
        .from('contacts')
        .select('id, full_name, phone, email')
        .eq('user_id', user.id)
        .order('full_name'),
      supabase
        .from('interview_questions')
        .select('*')
        .or(`user_id.eq.${user.id},is_system.eq.true`)
        .order('category'),
      supabase
        .from('circles')
        .select('id, name, color, members:circle_members(contact_id)')
        .eq('user_id', user.id)
        .order('name'),
    ])

    setSessions(sessionsRes.data || [])
    setContacts(contactsRes.data || [])
    setQuestions(questionsRes.data || [])
    setCircles(circlesRes.data || [])
    setLoading(false)
  }

  // Group sessions by interview_group_id for display
  const groupedSessions = sessions.reduce((acc, session) => {
    if (session.interview_group_id) {
      const existing = acc.find(g => g.group_id === session.interview_group_id)
      if (existing) {
        existing.sessions.push(session)
      } else {
        acc.push({
          group_id: session.interview_group_id,
          question: session.group_question || session.session_questions?.[0]?.question_text || 'Group Interview',
          sessions: [session],
          created_at: session.created_at,
        })
      }
    }
    return acc
  }, [] as GroupedSession[])

  // Individual sessions (not part of a group)
  const individualSessions = sessions.filter(s => !s.interview_group_id)

  const filteredQuestions = selectedCategory 
    ? questions.filter(q => 
        selectedCategory === 'custom' ? !q.is_system : q.category === selectedCategory
      )
    : []

  const toggleContactSelection = (contact: Contact) => {
    if (recipientMode === 'single') {
      setSelectedContacts([contact])
      setStep('question')
    } else {
      setSelectedContacts(prev => {
        const exists = prev.find(c => c.id === contact.id)
        if (exists) {
          return prev.filter(c => c.id !== contact.id)
        } else {
          return [...prev, contact]
        }
      })
    }
  }

  const getSelectedRecipients = (): Contact[] => {
    return selectedContacts
  }

  const handleCreateSession = async () => {
    const recipients = getSelectedRecipients()
    if (recipients.length === 0 || (!selectedQuestion && !customQuestion.trim())) return

    setCreating(true)
    const { data: { user } } = await supabase.auth.getUser()
    if (!user) return

    try {
      // Determine question text
      let questionText = ''
      let questionId = selectedQuestion
      
      if (customQuestion.trim()) {
        const { data: savedQuestion } = await supabase
          .from('interview_questions')
          .insert({
            user_id: user.id,
            question_text: customQuestion.trim(),
            category: 'custom',
            is_system: false
          })
          .select()
          .single()
        
        questionId = savedQuestion?.id || null
        questionText = customQuestion.trim()
      } else {
        const question = questions.find(q => q.id === selectedQuestion)
        questionText = question?.question_text || ''
      }

      // Generate group ID if multiple recipients
      const groupId = recipients.length > 1 ? crypto.randomUUID() : null

      // Create interview session for each recipient
      for (const recipient of recipients) {
        const { data: session, error: sessionError } = await supabase
          .from('interview_sessions')
          .insert({
            user_id: user.id,
            contact_id: recipient.id,
            title: recipients.length > 1 
              ? `${questionText.slice(0, 50)}...` 
              : `Interview with ${recipient.full_name}`,
            status: 'pending',
            interview_group_id: groupId,
            group_question: groupId ? questionText : null,
          })
          .select()
          .single()

        if (sessionError) throw sessionError

        // Add question to session
        await supabase.from('session_questions').insert({
          session_id: session.id,
          question_id: questionId,
          question_text: questionText,
          sort_order: 0,
        })
      }

      closeModal()
      loadData()
    } catch (error) {
      console.error('Error creating session:', error)
      alert('Failed to create interview')
    } finally {
      setCreating(false)
    }
  }

  const closeModal = () => {
    setShowNewSession(false)
    setStep('recipients')
    setRecipientMode('single')
    setSelectedContacts([])
    setSelectedCircle(null)
    setSelectedQuestion(null)
    setSelectedCategory(null)
    setCustomQuestion('')
  }

  const copyLink = (token: string, sessionId: string) => {
    navigator.clipboard.writeText(`${window.location.origin}/interview/${token}`)
    setCopied(sessionId)
    setTimeout(() => setCopied(null), 2000)
  }

  const shareViaEmail = (token: string, recipientName: string) => {
    const url = `${window.location.origin}/interview/${token}`
    const subject = encodeURIComponent(`I'd love to hear your story`)
    const body = encodeURIComponent(
      `Hi ${recipientName},\n\n` +
      `I'd love to hear your story! Please click the link below to record your responses:\n\n` +
      `${url}\n\n` +
      `Thank you!`
    )
    window.open(`mailto:?subject=${subject}&body=${body}`)
  }

  const shareViaSMS = (token: string) => {
    const url = `${window.location.origin}/interview/${token}`
    const text = encodeURIComponent(`I'd love to hear your story! Click here to share: ${url}`)
    window.open(`sms:?body=${text}`)
  }

  const getStatusStyle = (status: string) => {
    switch (status) {
      case 'completed': return 'bg-[#406A56]/10 text-[#406A56]'
      case 'recording': return 'bg-blue-50 text-blue-600'
      case 'sent': return 'bg-[#D9C61A]/10 text-[#9a8c12]'
      default: return 'bg-gray-100 text-gray-500'
    }
  }

  const proceedToQuestion = () => {
    const recipients = getSelectedRecipients()
    if (recipients.length > 0) {
      setStep('question')
    }
  }

  return (
    <div className="bg-[#F2F1E5] pb-8">
      {/* Header */}
      <header className="px-6 py-6">
        <div className="max-w-4xl mx-auto flex items-center justify-between">
          <div className="flex items-center gap-4">
            <Link 
              href="/dashboard" 
              className="p-2.5 bg-white/80 backdrop-blur-sm rounded-xl text-[#406A56] hover:bg-white transition-all shadow-sm"
            >
              <ChevronLeft size={20} />
            </Link>
            <div>
              <h1 className="text-2xl font-bold text-[#2d2d2d]">Interviews</h1>
              <p className="text-[#666] text-sm">Capture family stories remotely</p>
            </div>
          </div>

          <button
            onClick={() => setShowNewSession(true)}
            className="flex items-center gap-2 px-5 py-2.5 bg-[#C35F33] hover:bg-[#a54d28] text-white rounded-xl transition-all shadow-md"
          >
            <Plus size={18} />
            <span className="hidden sm:inline">New Interview</span>
          </button>
        </div>
      </header>

      {/* Main Content */}
      <main className="px-6 pb-12">
        <div className="max-w-4xl mx-auto">
          {loading ? (
            <div className="flex items-center justify-center h-64">
              <div className="text-[#666]">Loading...</div>
            </div>
          ) : sessions.length === 0 ? (
            <div className="bg-white/80 backdrop-blur-sm rounded-2xl p-12 text-center shadow-sm">
              <div className="w-16 h-16 bg-[#4A3552]/10 rounded-full flex items-center justify-center mx-auto mb-4">
                <Video size={32} className="text-[#4A3552]" />
              </div>
              <h3 className="text-lg font-semibold text-[#2d2d2d] mb-2">No interviews yet</h3>
              <p className="text-[#666] mb-6 max-w-md mx-auto">
                Send a question to family or friends, and they can record a video response from anywhere.
              </p>
              <button
                onClick={() => setShowNewSession(true)}
                className="inline-flex items-center gap-2 px-5 py-2.5 bg-[#C35F33] hover:bg-[#a54d28] text-white rounded-xl transition-all"
              >
                <Plus size={18} />
                Start your first interview
              </button>
            </div>
          ) : (
            <div className="space-y-4">
              {/* Grouped Interviews */}
              {groupedSessions.map((group) => (
                <motion.div
                  key={group.group_id}
                  initial={{ opacity: 0, y: 10 }}
                  animate={{ opacity: 1, y: 0 }}
                  className="bg-white/80 backdrop-blur-sm rounded-2xl p-5 shadow-sm hover:shadow-md transition-all"
                >
                  <div className="flex items-start justify-between gap-4 mb-4">
                    <div className="flex items-start gap-4">
                      <div className="w-12 h-12 rounded-full bg-gradient-to-br from-[#406A56] to-[#5a8a70] flex items-center justify-center text-white">
                        <Users size={24} />
                      </div>
                      <div>
                        <h3 className="text-[#2d2d2d] font-semibold">Group Interview</h3>
                        <p className="text-[#888] text-sm">{group.sessions.length} people</p>
                        <div className="flex items-center gap-2 mt-1">
                          <span className="text-xs text-[#406A56]">
                            {group.sessions.filter(s => s.status === 'completed').length} of {group.sessions.length} completed
                          </span>
                        </div>
                      </div>
                    </div>
                    <Link
                      href={`/dashboard/journalist/group/${group.group_id}`}
                      className="flex items-center gap-2 px-3 py-1.5 bg-[#406A56] hover:bg-[#2d5442] text-white text-sm rounded-lg transition-all"
                    >
                      <Play size={14} />
                      Story Time
                    </Link>
                  </div>
                  
                  <div className="pt-3 border-t border-gray-100">
                    <p className="text-sm text-[#666] italic mb-3">"{group.question}"</p>
                    <div className="flex flex-wrap gap-2">
                      {group.sessions.map(session => (
                        <div 
                          key={session.id}
                          className={`flex items-center gap-2 px-3 py-1.5 rounded-full text-xs ${
                            session.status === 'completed' 
                              ? 'bg-[#406A56]/10 text-[#406A56]' 
                              : 'bg-gray-100 text-gray-600'
                          }`}
                        >
                          {session.status === 'completed' && <Check size={12} />}
                          {session.contact?.full_name}
                        </div>
                      ))}
                    </div>
                  </div>
                </motion.div>
              ))}

              {/* Individual Interviews */}
              {individualSessions.map((session) => (
                <motion.div
                  key={session.id}
                  initial={{ opacity: 0, y: 10 }}
                  animate={{ opacity: 1, y: 0 }}
                  className="bg-white/80 backdrop-blur-sm rounded-2xl p-5 shadow-sm hover:shadow-md transition-all"
                >
                  <div className="flex items-start justify-between gap-4">
                    <div className="flex items-start gap-4">
                      <div className="w-12 h-12 rounded-full bg-gradient-to-br from-[#4A3552] to-[#6b4a7a] flex items-center justify-center text-white font-semibold">
                        {session.contact?.full_name?.charAt(0) || '?'}
                      </div>
                      <div>
                        <h3 className="text-[#2d2d2d] font-semibold">{session.title}</h3>
                        <p className="text-[#888] text-sm">with {session.contact?.full_name}</p>
                        <div className="flex items-center gap-3 mt-2">
                          <span className={`text-xs px-2.5 py-1 rounded-full font-medium capitalize ${getStatusStyle(session.status)}`}>
                            {session.status}
                          </span>
                          {session.video_responses?.length > 0 && (
                            <span className="text-[#406A56] text-xs flex items-center gap-1">
                              <CheckCircle size={12} />
                              Answered
                            </span>
                          )}
                        </div>
                      </div>
                    </div>

                    <div className="flex items-center gap-2">
                      {session.status === 'pending' && (
                        <div className="flex flex-col items-end gap-2">
                          {/* Shareable URL */}
                          <div className="flex items-center gap-2 bg-[#F2F1E5] rounded-lg px-3 py-2 border border-[#E8E7DC]">
                            <span className="text-sm text-[#666] truncate max-w-[200px] sm:max-w-[280px]">
                              {`${typeof window !== 'undefined' ? window.location.origin : ''}/interview/${session.access_token}`}
                            </span>
                            <button
                              onClick={() => copyLink(session.access_token, session.id)}
                              className="p-1.5 hover:bg-[#406A56]/10 rounded-md transition-all text-[#406A56]"
                              title="Copy link"
                            >
                              <Copy size={14} />
                            </button>
                          </div>
                          {/* Share Buttons */}
                          <div className="flex items-center gap-2">
                            <button
                              onClick={() => shareViaEmail(session.access_token, session.contact?.full_name || 'there')}
                              className="flex items-center gap-1.5 px-3 py-1.5 bg-[#406A56]/10 hover:bg-[#406A56]/20 text-[#406A56] text-sm rounded-lg transition-all"
                              title="Share via Email"
                            >
                              <Mail size={14} />
                              <span className="hidden sm:inline">Email</span>
                            </button>
                            <button
                              onClick={() => shareViaSMS(session.access_token)}
                              className="flex items-center gap-1.5 px-3 py-1.5 bg-[#406A56]/10 hover:bg-[#406A56]/20 text-[#406A56] text-sm rounded-lg transition-all"
                              title="Share via SMS"
                            >
                              <MessageSquare size={14} />
                              <span className="hidden sm:inline">SMS</span>
                            </button>
                            {copied === session.id && (
                              <span className="text-xs text-[#406A56] font-medium">Copied!</span>
                            )}
                          </div>
                        </div>
                      )}
                      {(session.status === 'sent' || session.status === 'completed') && (
                        <Link
                          href={`/dashboard/journalist/${session.id}?followup=true`}
                          className="flex items-center gap-2 px-3 py-1.5 bg-[#4A3552] hover:bg-[#5d4466] text-white text-sm rounded-lg transition-all"
                        >
                          <Plus size={14} />
                          Ask More
                        </Link>
                      )}
                      <Link
                        href={`/dashboard/journalist/${session.id}`}
                        className="flex items-center gap-2 px-3 py-1.5 bg-[#406A56]/10 hover:bg-[#406A56]/20 text-[#406A56] text-sm rounded-lg transition-all"
                      >
                        View
                      </Link>
                    </div>
                  </div>

                  {session.session_questions?.[0] && (
                    <div className="mt-4 pt-4 border-t border-gray-100">
                      <p className="text-sm text-[#666] italic">
                        "{session.session_questions[0].question_text}"
                      </p>
                    </div>
                  )}
                </motion.div>
              ))}
            </div>
          )}
        </div>
      </main>

      {/* New Interview Modal */}
      <AnimatePresence>
        {showNewSession && (
          <motion.div
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            exit={{ opacity: 0 }}
            className="fixed inset-0 bg-black/40 backdrop-blur-sm flex items-center justify-center z-50 p-4"
            onClick={closeModal}
          >
            <motion.div
              initial={{ scale: 0.95, opacity: 0 }}
              animate={{ scale: 1, opacity: 1 }}
              exit={{ scale: 0.95, opacity: 0 }}
              className="bg-[#F2F1E5] rounded-2xl max-w-lg w-full max-h-[85vh] overflow-hidden shadow-xl"
              onClick={e => e.stopPropagation()}
            >
              {/* Modal Header */}
              <div className="flex items-center justify-between px-6 py-4 border-b border-[#E8E7DC]">
                <div>
                  <h2 className="text-lg font-semibold text-[#2d2d2d]">New Interview</h2>
                  <p className="text-sm text-[#888]">
                    {step === 'recipients' ? 'Choose who to interview' : 'Pick a question to ask'}
                  </p>
                </div>
                <button onClick={closeModal} className="p-2 hover:bg-white/50 rounded-lg transition-all">
                  <X size={20} className="text-[#666]" />
                </button>
              </div>

              {/* Step 1: Select Recipients */}
              {step === 'recipients' && (
                <div className="flex flex-col h-[calc(85vh-80px)]">
                  {/* Mode Selector */}
                  <div className="flex gap-2 p-4 border-b border-[#E8E7DC]">
                    <button
                      onClick={() => { setRecipientMode('single'); setSelectedContacts([]); setSelectedCircle(null) }}
                      className={`flex-1 flex items-center justify-center gap-2 py-2.5 rounded-xl text-sm font-medium transition-all ${
                        recipientMode === 'single' ? 'bg-[#406A56] text-white' : 'bg-white/70 text-[#666]'
                      }`}
                    >
                      <User size={16} />
                      One Person
                    </button>
                    <button
                      onClick={() => { setRecipientMode('multiple'); setSelectedContacts([]); setSelectedCircle(null) }}
                      className={`flex-1 flex items-center justify-center gap-2 py-2.5 rounded-xl text-sm font-medium transition-all ${
                        recipientMode === 'multiple' ? 'bg-[#406A56] text-white' : 'bg-white/70 text-[#666]'
                      }`}
                    >
                      <Users size={16} />
                      Multiple
                    </button>
                    <button
                      onClick={() => { setRecipientMode('circle'); setSelectedContacts([]); setSelectedCircle(null) }}
                      className={`flex-1 flex items-center justify-center gap-2 py-2.5 rounded-xl text-sm font-medium transition-all ${
                        recipientMode === 'circle' ? 'bg-[#406A56] text-white' : 'bg-white/70 text-[#666]'
                      }`}
                    >
                      <Heart size={16} />
                      Circle
                    </button>
                  </div>

                  <div className="flex-1 overflow-y-auto p-4">
                    {/* Circle Selection */}
                    {recipientMode === 'circle' && (
                      <div className="space-y-4">
                        {circles.length === 0 ? (
                          <div className="text-center py-8">
                            <Heart size={40} className="text-[#888] mx-auto mb-3" />
                            <p className="text-[#666] mb-4">No circles yet</p>
                            <Link href="/dashboard/circles" className="text-[#C35F33] hover:underline">
                              Create a circle first ‚Üí
                            </Link>
                          </div>
                        ) : (
                          <>
                            <p className="text-sm text-[#666] mb-2">Select a circle:</p>
                            <div className="grid grid-cols-2 gap-2 mb-4">
                              {circles.map((circle) => (
                                <button
                                  key={circle.id}
                                  onClick={() => {
                                    setSelectedCircle(circle)
                                    // Auto-select all members in the circle
                                    const memberContacts = contacts.filter(c => 
                                      circle.members.some(m => m.contact_id === c.id)
                                    )
                                    setSelectedContacts(memberContacts)
                                  }}
                                  className={`flex items-center gap-3 p-3 rounded-xl transition-all text-left ${
                                    selectedCircle?.id === circle.id 
                                      ? 'bg-[#406A56]/10 ring-2 ring-[#406A56]' 
                                      : 'bg-white/70 hover:bg-white'
                                  }`}
                                >
                                  <div 
                                    className="w-8 h-8 rounded-full flex items-center justify-center"
                                    style={{ backgroundColor: circle.color + '20', color: circle.color }}
                                  >
                                    <Heart size={16} />
                                  </div>
                                  <div>
                                    <p className="font-medium text-[#2d2d2d]">{circle.name}</p>
                                    <p className="text-xs text-[#888]">{circle.members.length} members</p>
                                  </div>
                                </button>
                              ))}
                            </div>
                            
                            {/* Show selected circle members */}
                            {selectedCircle && selectedContacts.length > 0 && (
                              <div className="border-t border-[#E8E7DC] pt-4">
                                <p className="text-sm text-[#666] mb-2">Members to interview ({selectedContacts.length}):</p>
                                <div className="space-y-2">
                                  {selectedContacts.map((contact) => (
                                    <div
                                      key={contact.id}
                                      className="flex items-center gap-3 p-3 bg-white/70 rounded-xl"
                                    >
                                      <div className={`w-6 h-6 rounded-full border-2 flex items-center justify-center border-[#406A56] bg-[#406A56]`}>
                                        <Check size={14} className="text-white" />
                                      </div>
                                      <div className="w-9 h-9 rounded-full bg-gradient-to-br from-[#4A3552] to-[#6b4a7a] flex items-center justify-center text-white text-sm font-medium">
                                        {contact.full_name.charAt(0)}
                                      </div>
                                      <span className="text-[#2d2d2d]">{contact.full_name}</span>
                                      <button
                                        onClick={() => setSelectedContacts(prev => prev.filter(c => c.id !== contact.id))}
                                        className="ml-auto p-1 text-[#888] hover:text-red-500"
                                      >
                                        <X size={14} />
                                      </button>
                                    </div>
                                  ))}
                                </div>
                              </div>
                            )}
                          </>
                        )}
                      </div>
                    )}

                    {/* Contact Selection (single or multiple) */}
                    {recipientMode !== 'circle' && (
                      contacts.length === 0 ? (
                        <div className="text-center py-8">
                          <User size={40} className="text-[#888] mx-auto mb-3" />
                          <p className="text-[#666] mb-4">No contacts yet</p>
                          <Link href="/dashboard/contacts" className="text-[#C35F33] hover:underline">
                            Add contacts first ‚Üí
                          </Link>
                        </div>
                      ) : (
                        <div className="space-y-2">
                          {contacts.map((contact) => {
                            const isSelected = selectedContacts.some(c => c.id === contact.id)
                            return (
                              <button
                                key={contact.id}
                                onClick={() => toggleContactSelection(contact)}
                                className={`w-full flex items-center gap-4 p-4 rounded-xl transition-all text-left ${
                                  isSelected ? 'bg-[#406A56]/10 ring-2 ring-[#406A56]' : 'bg-white/70 hover:bg-white'
                                }`}
                              >
                                {recipientMode === 'multiple' && (
                                  <div className={`w-6 h-6 rounded-full border-2 flex items-center justify-center ${
                                    isSelected ? 'border-[#406A56] bg-[#406A56]' : 'border-gray-300'
                                  }`}>
                                    {isSelected && <Check size={14} className="text-white" />}
                                  </div>
                                )}
                                <div className="w-11 h-11 rounded-full bg-gradient-to-br from-[#4A3552] to-[#6b4a7a] flex items-center justify-center text-white font-medium">
                                  {contact.full_name.charAt(0)}
                                </div>
                                <div>
                                  <p className="text-[#2d2d2d] font-medium">{contact.full_name}</p>
                                  <p className="text-[#888] text-sm">{contact.phone || contact.email || 'No contact info'}</p>
                                </div>
                              </button>
                            )
                          })}
                        </div>
                      )
                    )}
                  </div>

                  {/* Continue button for multiple mode */}
                  {recipientMode === 'multiple' && (
                    <div className="p-4 border-t border-[#E8E7DC]">
                      <button
                        onClick={proceedToQuestion}
                        disabled={getSelectedRecipients().length === 0}
                        className="w-full py-3 bg-[#406A56] hover:bg-[#2d5442] text-white rounded-xl font-medium transition-all disabled:opacity-50 disabled:cursor-not-allowed"
                      >
                        Continue with {getSelectedRecipients().length} {getSelectedRecipients().length === 1 ? 'person' : 'people'}
                      </button>
                    </div>
                  )}
                </div>
              )}

              {/* Step 2: Select Question */}
              {step === 'question' && (
                <div className="flex flex-col h-[calc(85vh-80px)]">
                  {/* Recipients summary + back */}
                  <div className="flex items-center justify-between px-6 py-3 bg-white/50">
                    <button
                      onClick={() => { setStep('recipients'); setSelectedCategory(null); setSelectedQuestion(null); setCustomQuestion('') }}
                      className="text-[#C35F33] text-sm hover:underline"
                    >
                      ‚Üê Change recipients
                    </button>
                    <div className="flex items-center gap-2 px-3 py-1.5 bg-[#4A3552]/10 rounded-full">
                      {getSelectedRecipients().length === 1 ? (
                        <>
                          <User size={14} className="text-[#4A3552]" />
                          <span className="text-[#4A3552] text-sm font-medium">{getSelectedRecipients()[0].full_name}</span>
                        </>
                      ) : (
                        <>
                          <Users size={14} className="text-[#4A3552]" />
                          <span className="text-[#4A3552] text-sm font-medium">{getSelectedRecipients().length} people</span>
                        </>
                      )}
                    </div>
                  </div>

                  <div className="flex-1 overflow-y-auto">
                    {/* Custom Question */}
                    <div className="px-6 pt-4 pb-3">
                      <label className="block text-sm font-medium text-[#2d2d2d] mb-2">
                        Write your own question:
                      </label>
                      <textarea
                        value={customQuestion}
                        onChange={(e) => { setCustomQuestion(e.target.value); setSelectedQuestion(null); setSelectedCategory(null) }}
                        placeholder="What's a story from your childhood that shaped who you are today?"
                        rows={2}
                        className="w-full px-4 py-3 bg-white border border-[#E8E7DC] rounded-xl text-[#2d2d2d] placeholder-[#aaa] focus:outline-none focus:ring-2 focus:ring-[#406A56] resize-none"
                      />
                    </div>

                    {/* Divider */}
                    <div className="flex items-center gap-3 px-6 py-2">
                      <div className="flex-1 h-px bg-[#E8E7DC]" />
                      <span className="text-[#888] text-xs">or browse by category</span>
                      <div className="flex-1 h-px bg-[#E8E7DC]" />
                    </div>

                    {/* Categories */}
                    <div className="px-6 py-3">
                      <div className="flex gap-2 overflow-x-auto pb-2 scrollbar-hide">
                        {CATEGORIES.map(cat => (
                          <button
                            key={cat.id}
                            onClick={() => { setSelectedCategory(cat.id); setCustomQuestion('') }}
                            className={`flex items-center gap-2 px-4 py-2 rounded-full whitespace-nowrap text-sm transition-all ${
                              selectedCategory === cat.id
                                ? 'bg-[#406A56] text-white'
                                : 'bg-white/70 text-[#666] hover:bg-white'
                            }`}
                          >
                            <span>{cat.emoji}</span>
                            {cat.label}
                          </button>
                        ))}
                      </div>
                    </div>

                    {/* Questions List */}
                    {selectedCategory && (
                      <div className="px-6 pb-4 space-y-2">
                        {filteredQuestions.map(q => (
                          <button
                            key={q.id}
                            onClick={() => { setSelectedQuestion(q.id); setCustomQuestion('') }}
                            className={`w-full p-4 rounded-xl text-left transition-all ${
                              selectedQuestion === q.id
                                ? 'bg-[#406A56]/10 ring-2 ring-[#406A56]'
                                : 'bg-white/70 hover:bg-white'
                            }`}
                          >
                            <p className="text-[#2d2d2d]">{q.question_text}</p>
                          </button>
                        ))}
                        {filteredQuestions.length === 0 && (
                          <p className="text-center text-[#888] py-4">No questions in this category</p>
                        )}
                      </div>
                    )}
                  </div>

                  {/* Create Button */}
                  <div className="p-4 border-t border-[#E8E7DC]">
                    <button
                      onClick={handleCreateSession}
                      disabled={creating || (!selectedQuestion && !customQuestion.trim())}
                      className="w-full py-3 bg-[#C35F33] hover:bg-[#a54d28] text-white rounded-xl font-medium transition-all disabled:opacity-50 disabled:cursor-not-allowed flex items-center justify-center gap-2"
                    >
                      {creating ? (
                        'Creating...'
                      ) : (
                        <>
                          <Sparkles size={18} />
                          {getSelectedRecipients().length > 1 
                            ? `Send to ${getSelectedRecipients().length} people`
                            : 'Create Interview'
                          }
                        </>
                      )}
                    </button>
                  </div>
                </div>
              )}
            </motion.div>
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  )
}
